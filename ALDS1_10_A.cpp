int vector_finder(std::vector<ll> vec, int number) {
  auto itr = std::find(vec.begin(), vec.end(), number);
  size_t index = std::distance( vec.begin(), itr );
  if (index != vec.size()) { // 発見できたとき
    return 1;
  }
  else { // 発見できなかったとき
    return 0;
  }
}

// メモ化再帰
int n; 
vector<ll> f;
void fibo(int i) {
  if(i == n + 1) return;
  ll tmp = f[i - 1] + f[i - 2];
  f[i] = tmp;
  fibo(i + 1);
}

int main() {
  cin >> n; f.resize(100);
  f[0] = 1; f[1] = 1;
  fibo(2);
  cout << f[n] << endl;
}