int vector_finder(std::vector<ll> vec, int number) {
  auto itr = std::find(vec.begin(), vec.end(), number);
  size_t index = std::distance( vec.begin(), itr );
  if (index != vec.size()) { // 発見できたとき
    return 1;
  }
  else { // 発見できなかったとき
    return 0;
  }
}

int main() {
  int N, K; cin >> N >> K;
  ll ans = 0;
  for(int k = 1; k <= N; ++k) {
    if(k - K <= 0) continue;
    ans += (k - K) * (N / k);
    ll A;
    if(K != 0) A = N % k - K + 1;
    else if(K == 0) A = N % k;
    if(A >= 0) ans += A;
  }
  cout << ans << endl;
}